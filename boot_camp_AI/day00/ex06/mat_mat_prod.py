def mat_mat_prod(x, y):
	if (len(x) == 0 or len(y) == 0 or x.shape[1] != len(y)):
		return None
	tab = 0
	for i in range(y.shape[1]):
		if (i == 0):
			tab = (mat_vec_prod(x,y[:, i].reshape(len(y), 1)))
		else:
			tab = np.append(tab,mat_vec_prod(x,y[:, i].reshape(len(y), 1)), axis=1)
	return tab

